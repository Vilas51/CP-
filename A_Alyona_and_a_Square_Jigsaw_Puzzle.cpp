void solve(){
   int n;
   cin>>n;
   ll ans=0;
   ll sum=0;
   for(int i=0;i<n;i++){
    int x;
    cin>>x;
    sum+=x;
    ll res=sqrt(sum);
    if(res%2!=0&&res*res==sum){
        ans++;
    }
   }
   cout<<ans<<endl;
}
int main(){
    ios::sync_with_stdio(0); cin.tie(0);
    int t; cin>>t;
    while(t--){
        solve();
    }
}
